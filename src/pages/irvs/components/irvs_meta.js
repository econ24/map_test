export const getLabel = d => {
	let regex  = /irvs_PF_(\d+)_?(\d*)/,
		match = regex.exec(d);
	if (match) {
		return `PF-${ match[1] }${ match[2] ? `.${ match[2] }` : `` }`
	}

	regex = /irvs_(\d+)_(\d+)_?(\d*)/;
	match = regex.exec(d);
	if (match) {
		return `${ match[1] }.${ match[2] }${ match[3] ? `.${ match[3] }` : `` }`
	}

	regex = /irvs_8(\w)_(\d+)_?(\d*)/;
	match = regex.exec(d);
	if (match) {
		return `8${ match[1] }.${ match[2] }${ match[3] ? `.${ match[3] }`: `` }`
	}

	return d;
}
